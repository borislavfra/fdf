#include "fdf.h"

int			word_count(char const *s, char c)
{
	int		wn;

	wn = 0;
	if (!s)
		return (0);
	while (*s)
	{
		while (*s == c)
			s++;
		if (*s != c && *s != '\0')
		{
			wn++;
			while (*s != c && *s != '\0')
				s++;
		}
	}
	return (wn);
}
