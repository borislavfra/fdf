#include "fdf.h"

t_vector3	v3_multiply(t_vector3 v1, t_vector3 v2)
{
	v1.x = v1.x * v2.x;
	v1.y = v1.y * v2.y;
	v1.z = v1.z * v2.z;
	return (v1);
}
