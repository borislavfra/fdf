#include "fdf.h"

t_vector3	to_pxl(t_vector3 p)
{
	t_vector3	n_p;

	n_p.x = ceil(1920 / 2 * (1 + p.x));
	n_p.y = ceil(1080 / 2 * (1 + p.y));
	n_p.z = ceil(p.z);
	return (n_p);
}
